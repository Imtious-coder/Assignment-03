// Problem-01(Kilometer to Meter Converter)
function kilometerToMeter(kiloMeter) {
    if (kiloMeter >= 0) {
        let result = kiloMeter * 1000;
        return result;
    }
    else {
        return "Please input a valid number."
    }
    return result;
}

